"use client";

import {
  type GalleryHandlers,
  type GalleryOptions,
  Gallery,
} from "@/components/ui/gallery";

export default function GalleryComponentPage() {
  const handleDownload = () => {
    alert("DOWNLOADING...");
  };
  const handleShare = () => {
    alert("SHARING...");
  };
  const handleClose = () => {
    alert("CLOSING...");
  };

  const options: GalleryOptions = {
    chevrons: true,
    close: true,
    download: true,
    share: true,
  };

  const handlers: GalleryHandlers = {
    onClose: handleClose,
    onDownload: handleDownload,
    onShare: handleShare,
  };

  return (
    <div className="h-screen w-screen">
      <Gallery {...options} {...handlers} />
    </div>
  );
}
